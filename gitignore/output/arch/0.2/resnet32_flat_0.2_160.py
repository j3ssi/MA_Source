import torch.nn as nn
__all__ = ['resnet32_flat']
class ResNet32(nn.Module):
	def __init__(self, num_classes=10):
		super(ResNet32, self).__init__()
		self.conv1 = nn.Conv2d(3, 9, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn1 = nn.BatchNorm2d(9)
		self.conv4 = nn.Conv2d(9, 2, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn4 = nn.BatchNorm2d(2)
		self.conv5 = nn.Conv2d(2, 9, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn5 = nn.BatchNorm2d(9)
		self.conv8 = nn.Conv2d(9, 1, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn8 = nn.BatchNorm2d(1)
		self.conv9 = nn.Conv2d(1, 9, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn9 = nn.BatchNorm2d(9)
		self.conv12 = nn.Conv2d(9, 14, kernel_size=3, stride=2, padding=1, bias=False)
		self.bn12 = nn.BatchNorm2d(14)
		self.conv13 = nn.Conv2d(14, 18, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn13 = nn.BatchNorm2d(18)
		self.conv14 = nn.Conv2d(9, 18, kernel_size=1, stride=2, padding=0, bias=False)
		self.bn14 = nn.BatchNorm2d(18)
		self.conv23 = nn.Conv2d(18, 23, kernel_size=3, stride=2, padding=1, bias=False)
		self.bn23 = nn.BatchNorm2d(23)
		self.conv24 = nn.Conv2d(23, 16, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn24 = nn.BatchNorm2d(16)
		self.conv25 = nn.Conv2d(18, 16, kernel_size=1, stride=2, padding=0, bias=False)
		self.bn25 = nn.BatchNorm2d(16)
		self.conv26 = nn.Conv2d(16, 11, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn26 = nn.BatchNorm2d(11)
		self.conv27 = nn.Conv2d(11, 16, kernel_size=3, stride=1, padding=1, bias=False)
		self.bn27 = nn.BatchNorm2d(16)
		self.avgpool = nn.AvgPool2d(8)
		self.relu = nn.ReLU(inplace=True)
		self.fc = nn.Linear(16, num_classes)
	def forward(self, x):
		x = self.conv1(x)
		x = self.bn1(x)
		_x = self.relu(x)
		x = self.conv4(_x)
		x = self.bn4(x)
		x = self.relu(x)
		x = self.conv5(x)
		x = self.bn5(x)
		_x = _x + x
		_x = self.relu(_x)
		x = self.conv8(_x)
		x = self.bn8(x)
		x = self.relu(x)
		x = self.conv9(x)
		x = self.bn9(x)
		_x = _x + x
		_x = self.relu(_x)
		x = self.conv12(_x)
		x = self.bn12(x)
		x = self.relu(x)
		x = self.conv13(x)
		x = self.bn13(x)
		_x = self.conv14(_x)
		_x = self.bn14(_x)
		_x = _x + x
		_x = self.relu(_x)
		x = self.conv23(_x)
		x = self.bn23(x)
		x = self.relu(x)
		x = self.conv24(x)
		x = self.bn24(x)
		_x = self.conv25(_x)
		_x = self.bn25(_x)
		_x = _x + x
		_x = self.relu(_x)
		x = self.conv26(_x)
		x = self.bn26(x)
		x = self.relu(x)
		x = self.conv27(x)
		x = self.bn27(x)
		_x = _x + x
		_x = self.relu(_x)
		x = self.avgpool(_x)
		x = x.view(x.size(0), -1)
		x = self.fc(x)
		return x
def resnet32_flat(**kwargs):
	model = ResNet32(**kwargs)
	return model
